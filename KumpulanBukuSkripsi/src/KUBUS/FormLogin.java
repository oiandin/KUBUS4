    private void jButtonLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLoginActionPerformed
        // TODO add your handling code here:
        String username = jTextFieldUsername.getText();
        String password = new String(jPasswordField.getPassword());

        EntityManagerFactory emf = Persistence.createEntityManagerFactory("KumpulanBukuSkripsiPU");
        EntityManager em = emf.createEntityManager();

        try {
            em.getTransaction().begin();

            // Assuming you have a User entity with attributes 'username' and 'password'
            Query q = em.createQuery("SELECT p FROM Petugas p WHERE p.username = :username AND p.password = :password");
            q.setParameter("username", username);
            q.setParameter("password", password);

            if (!q.getResultList().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Anda berhasil masuk");

                new Dashboard().setVisible(true);
                this.dispose();

                kosongkan_form();
            } else {
                JOptionPane.showMessageDialog(this, "Login invalid, periksa kembali username dan password Anda");

                kosongkan_form();
            }

            em.getTransaction().commit();
        } finally {
            em.close();
            emf.close();
        }
    }//GEN-LAST:event_jButtonLoginActionPerformed

